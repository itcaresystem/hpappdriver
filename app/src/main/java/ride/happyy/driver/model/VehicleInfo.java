package ride.happyy.driver.model;

public class VehicleInfo {
    private String vehicle_brand;
    private String vehicle_type;
    private String vehicle_no;
    private String car_fitness_certificate;
    private String driving_license_number;
    private String phone;

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getVehicle_brand() {
        return vehicle_brand;
    }

    public void setVehicle_brand(String vehicle_brand) {
        this.vehicle_brand = vehicle_brand;
    }

    public String getVehicle_type() {
        return vehicle_type;
    }

    public void setVehicle_type(String vehicle_type) {
        this.vehicle_type = vehicle_type;
    }

    public String getVehicle_no() {
        return vehicle_no;
    }

    public void setVehicle_no(String vehicle_no) {
        this.vehicle_no = vehicle_no;
    }

    public String getCar_fitness_certificate() {
        return car_fitness_certificate;
    }

    public void setCar_fitness_certificate(String car_fitness_certificate) {
        this.car_fitness_certificate = car_fitness_certificate;
    }

    public String getDriving_license_number() {
        return driving_license_number;
    }

    public void setDriving_license_number(String driving_license_number) {
        this.driving_license_number = driving_license_number;
    }
}
